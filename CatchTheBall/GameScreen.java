/**
 * Enum representing the different screens / states of the game.
 * Controls which screen is currently rendered and active.
 *
 * Demonstrates: Enum used as a state machine (OOP principle)
 */
public enum GameScreen {
    NAME_ENTRY,
    MENU,
    INSTRUCTIONS,
    PLAYING,
    SHOP,
    PAUSED,
    GAME_OVER,
    LEADERBOARD
}
